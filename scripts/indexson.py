#!/usr/bin/env python3
"""
indexSON.py

Builds a lightweight index.json file from locally stored JacSON profile JSON files.

Expected local structure:
    ./profiles/<semester_code>/<full_course_code>.json

Example:
    ./profiles/7620/MGTS1301-20352-7620.json

Output:
    ./profiles/index.json

This script does NOT hit any remote servers.
It reads only from the local ./profiles/ directory.

Compatible with profiles generated by both the original JacSON scraper
and the enriched Sean Smith / UQBS scraper (jacson.py v2+).
"""

from __future__ import annotations

import json
import os
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional


# =============================================================================
# CONFIGURATION
# =============================================================================

# --- For ./scripts/ directory run ---
SCRIPT_DIR = os.path.abspath(os.path.dirname(__file__))
PROJECT_ROOT = os.path.abspath(os.path.join(SCRIPT_DIR, ".."))
PROFILES_DIR = os.path.join(PROJECT_ROOT, "profiles")
OUTPUT_FILE = os.path.join(PROJECT_ROOT, "profiles", "index.json")
INDEX_VERSION = "1.1"

